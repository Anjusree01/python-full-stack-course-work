print('------------------------------------------NUMERICAL-------------------------------------------------:')
print('int : combination of numbers', 10, sep = '\n')
int = 10
print(int) #10
type(int) #integer

print('float : decimal values', 8.2, sep = '\n')
f = 10.2
print(f) #10.2
type(f) #float

print('complex : imaginary values',3+3j, sep = '\n', end = '\n\n')
c = 10+2j
print(c) #10+2j
type(c) #complex


print('-----------------------------------------SEQUENTIAL DT---------------------------------------------:')
print('string : combination of characters', 'Anjusree', sep = '\n')
a = 'abc'
print(a) #'abc'
type(a) #string

print('list : collection of items', [2,3,4,5], sep = '\n')
l = [1,2,3,4]
print(l) #[1,2,3,4]
type(l) #list

print('tuple : collection of items', (2,3,4,5), sep = '\n', end = '\n\n')
t = (1,2,3,4)
print(t) #(1,2,3,4)
type(t) #tuple

print('-------------------------------------------MAPPING DT---------------------------------------------:')
print('set : collection of items', {2,3,4,5}, sep = '\n')
s = {1,2,3,4}
print(s) #{1,2,3,4}
type(s) #set

print('dictionary : stores data in key-value pair', {'1':'1','2':'4','3':'9','4':'16'}, sep = '\n', end = '\n\n')
d = {1:1, 2:4, 3:9}
print(d) #{1:1, 2:4, 3:9}
type(d) #dict

