a= 3
b= 8
c= 0
print(a,b,c) #comma separation
print(f'a={a} b={b} c={c}') #f string
print('a=%d b=%f c=%s' %(a,b,c)) #modulo separation
print('a=%d b=%d c=%d' %(a,b,c)) #modulo separation
print('a={}, b={}, c={}'.format(a,b,c)) #formating
print('a={2}, b={0}, c={1}'.format(a,b,c)) #formating
